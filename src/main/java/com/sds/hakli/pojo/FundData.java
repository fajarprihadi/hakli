package com.sds.hakli.pojo;

public class FundData {

	private String sourceAccount;
	private String sourceAccountName;
	private String sourceAccountStatus;
	private String sourceAccountBalance;
	private String beneficiaryAccount;
	private String beneficiaryAccountName;
	private String beneficiaryAccountStatus;
	
	public String getSourceAccount() {
		return sourceAccount;
	}
	public void setSourceAccount(String sourceAccount) {
		this.sourceAccount = sourceAccount;
	}
	public String getSourceAccountName() {
		return sourceAccountName;
	}
	public void setSourceAccountName(String sourceAccountName) {
		this.sourceAccountName = sourceAccountName;
	}
	public String getSourceAccountStatus() {
		return sourceAccountStatus;
	}
	public void setSourceAccountStatus(String sourceAccountStatus) {
		this.sourceAccountStatus = sourceAccountStatus;
	}
	public String getSourceAccountBalance() {
		return sourceAccountBalance;
	}
	public void setSourceAccountBalance(String sourceAccountBalance) {
		this.sourceAccountBalance = sourceAccountBalance;
	}
	public String getBeneficiaryAccount() {
		return beneficiaryAccount;
	}
	public void setBeneficiaryAccount(String beneficiaryAccount) {
		this.beneficiaryAccount = beneficiaryAccount;
	}
	public String getBeneficiaryAccountName() {
		return beneficiaryAccountName;
	}
	public void setBeneficiaryAccountName(String beneficiaryAccountName) {
		this.beneficiaryAccountName = beneficiaryAccountName;
	}
	public String getBeneficiaryAccountStatus() {
		return beneficiaryAccountStatus;
	}
	public void setBeneficiaryAccountStatus(String beneficiaryAccountStatus) {
		this.beneficiaryAccountStatus = beneficiaryAccountStatus;
	}
	
	
}
