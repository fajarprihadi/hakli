package com.sds.hakli.pojo;

public class FundRcData {
	
	private String noReferral;
	private String journalSeq;
	private String amount;
	private String remark;
	private String internalTransferStatus;
	private String internalTransferDescription;
	
	public String getNoReferral() {
		return noReferral;
	}
	public void setNoReferral(String noReferral) {
		this.noReferral = noReferral;
	}
	public String getJournalSeq() {
		return journalSeq;
	}
	public void setJournalSeq(String journalSeq) {
		this.journalSeq = journalSeq;
	}
	public String getAmount() {
		return amount;
	}
	public void setAmount(String amount) {
		this.amount = amount;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	public String getInternalTransferStatus() {
		return internalTransferStatus;
	}
	public void setInternalTransferStatus(String internalTransferStatus) {
		this.internalTransferStatus = internalTransferStatus;
	}
	public String getInternalTransferDescription() {
		return internalTransferDescription;
	}
	public void setInternalTransferDescription(String internalTransferDescription) {
		this.internalTransferDescription = internalTransferDescription;
	}

	
}
